
public class Casting {

	public static void main(String[] args) {
		
		int i=10;
		
		float f = i;
		
		i=(int)f;
		
		int j=100;
		
		long l=j;
		
		long m=1000000000000000000l;
		
		i=(int)m;
		
		float n=10.5f;
		
		double d=12.5;
		
		n=(float)d;
		
		d=f;
		
		
		

	}

}
